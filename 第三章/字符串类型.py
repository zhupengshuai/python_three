#转义字符
print('宁波大学')
print('欢迎你')
print('_____________')
print('宁波大学\n欢迎你')#遇到\n即表示换行
print('宁波大学\t欢迎你')#遇到\t即表示空格
print('hello\toooo')#一个制表位有8个字符,hello有五个字符，故\t会空8-5=3格
print('hettoooooooo')
print('老师说：\'好好学习，天天向上\'')
print('老师说：\"好好学习，天天向上\"')#\'和\"即表示给说的话带上引号
#原字符：是转义字符失效的字符，一般用R或r
print('--------------------------------------')
print(R'宁波大学\n欢迎你')
print(r'宁波大学\t欢迎你')